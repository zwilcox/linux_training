int main(int argc, char *argv[]) {
    FILE *fd = NULL;
    char buf[260] = {0};
    char *contents;
    size_t chars_read = 0;
    int pipe_fd;

    if (argc < 2) {
        printf("You need 2 arguments!\n");
        return -1;
    }

    pipe_fd = atoi(argv[1]);
    pid_t parent = getppid();
    sprintf(buf, "/proc/%d/comm", parent);
    fd = fopen(buf, "rb");
    if (!fd) {
        perror("fopen");
        return -1;
    }
    if (getline(&contents, &chars_read, fd) == -1) {
        perror("getline");
        return -1;
    }
    fclose(fd);

    if (strncmp(contents, "bash", 4) == 0) {
        printf("You didn't execv this program! You get no flag!\n");
        return EXIT_FAILURE;
    }

	// Students could run it in gdb and then just do `r 1` and it would spit out the flag
	// So we check for gdb as well
    if (strncmp(contents, "gdb", 3) == 0) {
        printf("You didn't execv this program! You get no flag!\n");
        return EXIT_FAILURE;
    }
    write(pipe_fd, "1412", 4);
    close(pipe_fd);
    
    
    free(contents);
     
    return 0;
}
